public static class MathBase
{
    public static int Sum(int x, int y)
    {
        return x + y;
    }
    public static double Sum(int x, int y,string f)
    {
        return x + y;
    }
    public static int Sum(string x, int y)
    {
        return int.Parse(x) + y;
    }
    public static int Sum(int x, int y, int z)
    {
        return x + y + z;
    }
    public static int Sum(int x, int y, int z, int h)
    {
        return x + y + z + h;
    }
}